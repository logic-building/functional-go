package fp

// MinInt returns min item from the list.
// Return 0 if the list is either empty or nil
func MinInt(list []int) int {
	if list == nil || len(list) == 0 {
		return 0
	}
	min := list[0]
	for _, v := range list {
		if v < min {
			min = v
		}
	}
	return min
}

