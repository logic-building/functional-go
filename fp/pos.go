package fp

// PosIntWht Returns true if num is greater than zero, else false
func PosIntWht(v int) bool {
	if v > 0 {
		return true
	}
	return false
}

// PosIntWhtPtr Returns true if num is greater than zero, else false
func PosIntWhtPtr(v *int) bool {
	if *v > 0 {
		return true
	}
	return false
}

