package fp

// PosWhtInt Returns true if num is greater than zero, else false
func PosWhtInt(v int) bool {
	if v > 0 {
		return true
	}
	return false
}

// PosWhtIntPtr Returns true if num is greater than zero, else false
func PosWhtIntPtr(v *int) bool {
	if *v > 0 {
		return true
	}
	return false
}

