package fp

// PosIntP Returns true if num is greater than zero, else false
func PosIntP(v int) bool {
	if v > 0 {
		return true
	}
	return false
}

// PosIntPPtr Returns true if num is greater than zero, else false
func PosIntPPtr(v *int) bool {
	if *v > 0 {
		return true
	}
	return false
}

