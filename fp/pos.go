package fp

// PosInt Returns true if num is greater than zero, else false
func PosInt(v int) bool {
	if v > 0 {
		return true
	}
	return false
}

// PosIntPtr Returns true if num is greater than zero, else false
func PosIntPtr(v *int) bool {
	if *v > 0 {
		return true
	}
	return false
}

