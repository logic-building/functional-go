package fp

// ZeroInt Returns true if num is zero, else false
func ZeroInt(v int) bool {
	if v == 0 {
		return true
	}
	return false
}

// ZeroIntPtr Returns true if num is zero, else false
func ZeroIntPtr(v *int) bool {
	if *v == 0 {
		return true
	}
	return false
}

