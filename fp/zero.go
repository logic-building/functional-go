package fp

// ZeroIntP Returns true if num is zero, else false
func ZeroIntP(v int) bool {
	if v == 0 {
		return true
	}
	return false
}

// ZeroIntPPtr Returns true if num is zero, else false
func ZeroIntPPtr(v *int) bool {
	if *v == 0 {
		return true
	}
	return false
}

