package fp

// NegInt Returns true if num is less than zero, else false
func NegInt(v int) bool {
	if v < 0 {
		return true
	}
	return false
}

// NegIntPtr Returns true if num is less than zero, else false
func NegIntPtr(v *int) bool {
	if *v < 0 {
		return true
	}
	return false
}

