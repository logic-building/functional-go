package fp

// NegIntWht Returns true if num is less than zero, else false
func NegIntWht(v int) bool {
	if v < 0 {
		return true
	}
	return false
}

// NegIntWhtPtr Returns true if num is less than zero, else false
func NegIntWhtPtr(v *int) bool {
	if *v < 0 {
		return true
	}
	return false
}

