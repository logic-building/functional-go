package fp

// NegIntP Returns true if num is less than zero, else false
func NegIntP(v int) bool {
	if v < 0 {
		return true
	}
	return false
}

// NegIntPPtr Returns true if num is less than zero, else false
func NegIntPPtr(v *int) bool {
	if *v < 0 {
		return true
	}
	return false
}

